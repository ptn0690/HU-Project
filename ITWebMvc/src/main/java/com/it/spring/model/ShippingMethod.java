package com.it.spring.model;

public class ShippingMethod {
    private int id;
    private String method;

    public ShippingMethod() {
    }
    
    //getter and setter
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getMethod() {
        return method;
    }

    public void setMethod(String method) {
        this.method = method;
    }
    
    
}
